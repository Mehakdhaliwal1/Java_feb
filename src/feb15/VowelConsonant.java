package feb15;

public class VowelConsonant {
    public static void main(String[] args) {
        char ch = 'g';
        if (ch == 'a' || ch == 'A' || ch == 'e' || ch == 'E' || ch == 'i' || ch == 'I' || ch == 'o' || ch == 'O'||
        ch == 'u' || ch == 'U'){
            System.out.println("Character is a vowel");
        }else{
            System.out.println("Character is  consonant");
        }
    }
}
